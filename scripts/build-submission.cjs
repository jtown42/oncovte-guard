#!/usr/bin/env node
/**
 * Assemble the per-field submission text files into one copy-paste document,
 * submission/SUBMISSION-FULL.md. Generated from the .txt field files so it can
 * never drift from the character-verified source. Run:
 *   node scripts/build-submission.cjs   (or: npm run build:submission)
 */
const fs = require("fs");
const path = require("path");

const DIR = path.join(__dirname, "..", "submission");
const OUT = path.join(DIR, "SUBMISSION-FULL.md");

// Ordered to mirror the official submission form. limit = null -> no stated cap.
const FIELDS = [
  ["01-abstract.txt", "Project abstract", 1000],
  ["02-rationale.txt", "Project rationale, impact and innovation", 3500],
  ["03-design.txt", "Project design and implementation", 7000],
  ["04-evaluation.txt", "Project evaluation and sustainability", 3500],
  ["05-audience.txt", "Who is the intended user/audience of your app?", null],
  ["06-twitter.txt", "Twitter project summary", 140],
  ["07-fhir-usage.txt", "How is FHIR being used in the app?", 500],
  ["08-fhir-release-resources.txt", "What FHIR release and resources does your application use?", 500],
  ["09-data-source.txt", "Data source for the FHIR resources and how they are accessed", 500],
  ["10-other-info.txt", "Any other information about the project", 1500],
  ["00-short-answers.txt", "Structured / short-answer fields", null],
];

const lines = [];
lines.push("# OncoVTE Guard — Full Submission (copy-paste ready)");
lines.push("");
lines.push(
  "> Generated by `npm run build:submission` from the verified field files in " +
    "`submission/`. Do not edit this file directly — edit the source `.txt` " +
    "files and regenerate. Character counts include spaces and newlines and are " +
    "checked by `npm run check:submission`.",
);
lines.push("");
lines.push("**Title:** OncoVTE Guard  |  **Category:** Student");
lines.push("");
lines.push("---");
lines.push("");

let anyOver = false;
for (const [file, title, limit] of FIELDS) {
  const p = path.join(DIR, file);
  const text = fs.existsSync(p) ? fs.readFileSync(p, "utf8") : "(MISSING)";
  const len = text.length;
  const over = limit != null && len > limit;
  if (over) anyOver = true;
  const meta =
    limit == null
      ? `${len} characters`
      : `${len} / ${limit} characters${over ? "  ** OVER LIMIT **" : ""}`;
  lines.push(`## ${title}`);
  lines.push("");
  lines.push(`_${meta}_`);
  lines.push("");
  lines.push("```text");
  lines.push(text);
  lines.push("```");
  lines.push("");
}

lines.push("---");
lines.push("");
lines.push("### Supporting artifacts");
lines.push("");
lines.push("- FHIR Capability Statement: `public/capability-statement.json`");
lines.push("- Promotional photos (5 decision states): `docs/screenshots/`");
lines.push("- Verification & traceability matrix: `VERIFICATION.md`");
lines.push("- Advisor attestation template: `submission/advisor-attestation-template.md`");
lines.push("");

fs.writeFileSync(OUT, lines.join("\n"), "utf8");
console.log(`Wrote ${path.relative(path.join(__dirname, ".."), OUT)}`);
if (anyOver) {
  console.error("WARNING: one or more fields are over their limit.");
  process.exit(1);
}
